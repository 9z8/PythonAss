t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.left(120)
t.forward(100) # draw base
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.forward(100)
t.right(90)
t.forward(300)
t.penup()
