import turtle
  
t = turtle.Turtle()
turtle.hideturtle()
t.penup()
t.goto(0, 0)
t.pendown()
t.circle(50)
t.penup()
t.goto(100, 0)
t.pendown()
t.circle(50)
t.penup()
t.forward(50)
t.right(90)
t.forward(300)
t.pendown()
t.backward(350)
t.penup()
t.right(90)
t.forward(200)
t.left(90)
t.pendown()
t.forward(350)
t.done()
